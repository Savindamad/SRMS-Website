package Other;
public class CombinationInfo {
    String tableNo,numOfSeats;

    public CombinationInfo(String tableNo, String numOfSeats) {
        this.tableNo = tableNo;
        this.numOfSeats = numOfSeats;
    }
    
    public String getTableNo() {
        return tableNo;
    }

    public String getNumOfSeats() {
        return numOfSeats;
    }
    
}
